<?php
/**
 * Export Helper Functions
 *
 * Functions for exporting data to Excel and PDF
 */

use PhpOffice\PhpSpreadsheet\Spreadsheet;
use PhpOffice\PhpSpreadsheet\Writer\Xlsx;
use PhpOffice\PhpSpreadsheet\Chart\Chart;
use PhpOffice\PhpSpreadsheet\Chart\DataSeries;
use PhpOffice\PhpSpreadsheet\Chart\DataSeriesValues;
use PhpOffice\PhpSpreadsheet\Chart\Legend;
use PhpOffice\PhpSpreadsheet\Chart\PlotArea;
use PhpOffice\PhpSpreadsheet\Chart\Title;
use PhpOffice\PhpSpreadsheet\Chart\Axis;
use PhpOffice\PhpSpreadsheet\Style\Color;
use PhpOffice\PhpSpreadsheet\Style\Fill;
use PhpOffice\PhpSpreadsheet\Style\Border;
use PhpOffice\PhpSpreadsheet\Style\Alignment;

/**
 * Export data to Excel
 *
 * @param string $filename The filename
 * @param array $data The data to export
 * @param string $type The report type (business_unit, agent, activity)
 * @return void
 */
function export_to_excel($filename, $data, $type) {
    // Debug line to check variables

    // Create new Spreadsheet object
    $spreadsheet = new Spreadsheet();

    // Set document properties
    $spreadsheet->getProperties()
        ->setCreator('QA Evaluation App')
        ->setLastModifiedBy('QA Evaluation App')
        ->setTitle('QA Evaluation Report')
        ->setSubject('QA Evaluation Report')
        ->setDescription('QA Evaluation Report generated by QA Evaluation App')
        ->setKeywords('qa evaluation report')
        ->setCategory('Report');

    // Get active sheet
    $sheet = $spreadsheet->getActiveSheet();
    $sheet->setTitle('Report');

    // Define starting position (data will start from cell C5 as requested)
    $Scol = 'C';
    $Srow = 5;

    // Set header style
    $headerStyle = [
        'font' => [
            'bold' => true,
            'color' => ['rgb' => 'FFFFFF'],
        ],
        'fill' => [
            'fillType' => Fill::FILL_SOLID,
            'startColor' => ['rgb' => '4472C4'],
        ],
        'borders' => [
            'allBorders' => [
                'borderStyle' => Border::BORDER_THIN,
                'color' => ['rgb' => '000000'],
            ],
        ],
        'alignment' => [
            'horizontal' => Alignment::HORIZONTAL_CENTER,
            'vertical' => Alignment::VERTICAL_CENTER,
        ],
    ];

    // Set data style
    $dataStyle = [
        'borders' => [
            'allBorders' => [
                'borderStyle' => Border::BORDER_THIN,
                'color' => ['rgb' => '000000'],
            ],
        ],
        'alignment' => [
            'vertical' => Alignment::VERTICAL_CENTER,
        ],
    ];

    // Add title based on report type
    $title = '';
    $dateRange = '';

    // Get date range if available
    if (isset($_GET['start_date']) && !empty($_GET['start_date']) && isset($_GET['end_date']) && !empty($_GET['end_date'])) {
        $dateRange = 'Date Range: ' . $_GET['start_date'] . ' to ' . $_GET['end_date'];
    } else {
        $dateRange = 'Date Range: All Time';
    }

    switch ($type) {
        case 'business_unit':
            $title = 'Business Unit Report: ' . $data['businessUnit']['name'];
            break;
        case 'agent':
            $title = 'Agent Report: ' . $data['agent']['name'];
            break;
        case 'activity':
            $title = 'Activity Report: ' . $data['activity']['name'];
            break;
    }

    $sheet->setCellValue($Scol . $Srow, $title);
    $sheet->mergeCells($Scol . $Srow . ':' . chr(ord($Scol) + 4) . $Srow);
    $sheet->getStyle($Scol . $Srow)->getFont()->setBold(true)->setSize(16);
    $sheet->getStyle($Scol . $Srow)->getAlignment()->setHorizontal(Alignment::HORIZONTAL_CENTER);

    $sheet->setCellValue($Scol . ($Srow + 1), $dateRange);
    $sheet->mergeCells($Scol . ($Srow + 1) . ':' . chr(ord($Scol) + 4) . ($Srow + 1));
    $sheet->getStyle($Scol . ($Srow + 1))->getFont()->setItalic(true);
    $sheet->getStyle($Scol . ($Srow + 1))->getAlignment()->setHorizontal(Alignment::HORIZONTAL_CENTER);

    // Add summary data
    $sheet->setCellValue($Scol . ($Srow + 2), 'Summary');
    $sheet->getStyle($Scol . ($Srow + 2))->getFont()->setBold(true)->setSize(14);

    $sheet->setCellValue($Scol . ($Srow + 3), 'Total Evaluations:');
    $sheet->setCellValue(chr(ord($Scol) + 1) . ($Srow + 3), $data['evaluationCount']);

    switch ($type) {
        case 'business_unit':
        case 'activity':
            $sheet->setCellValue($Scol . ($Srow + 4), 'Average Score:');
            $sheet->setCellValue(chr(ord($Scol) + 1) . ($Srow + 4), number_format($data['overallAverageScore'], 2) . '%');
            break;
        case 'agent':
            $sheet->setCellValue($Scol . ($Srow + 4), 'Average Score:');
            $sheet->setCellValue(chr(ord($Scol) + 1) . ($Srow + 4), number_format($data['averageScore'], 2) . '%');
            break;
    }

    // Add evaluations data
    $sheet->setCellValue($Scol . ($Srow + 6), 'Evaluations');
    $sheet->getStyle($Scol . ($Srow + 6))->getFont()->setBold(true)->setSize(14);

    // Set headers based on report type
    $row = $Srow + 7;
    switch ($type) {
        case 'business_unit':
            $sheet->setCellValue($Scol . $row, 'Date');
            $sheet->setCellValue(chr(ord($Scol) + 1) . $row, 'Agent');
            $sheet->setCellValue(chr(ord($Scol) + 2) . $row, 'Activity');
            $sheet->setCellValue(chr(ord($Scol) + 3) . $row, 'Evaluator');
            $sheet->setCellValue(chr(ord($Scol) + 4) . $row, 'Score');
            break;
        case 'agent':
            $sheet->setCellValue($Scol . $row, 'Date');
            $sheet->setCellValue(chr(ord($Scol) + 1) . $row, 'Activity');
            $sheet->setCellValue(chr(ord($Scol) + 2) . $row, 'Evaluator');
            $sheet->setCellValue(chr(ord($Scol) + 3) . $row, 'Score');
            $sheet->setCellValue(chr(ord($Scol) + 4) . $row, 'Comments');
            break;
        case 'activity':
            $sheet->setCellValue($Scol . $row, 'Date');
            $sheet->setCellValue(chr(ord($Scol) + 1) . $row, 'Agent');
            $sheet->setCellValue(chr(ord($Scol) + 2) . $row, 'Evaluator');
            $sheet->setCellValue(chr(ord($Scol) + 3) . $row, 'Score');
            $sheet->setCellValue(chr(ord($Scol) + 4) . $row, 'Comments');
            break;
    }

    // Apply header style
    $sheet->getStyle($Scol . $row . ':' . chr(ord($Scol) + 4) . $row)->applyFromArray($headerStyle);

    // Add data
    $row++;
    $startRow = $row;

    foreach ($data['evaluations'] as $evaluation) {
        switch ($type) {
            case 'business_unit':
                $sheet->setCellValue($Scol . $row, date('Y-m-d', strtotime($evaluation['evaluation_date'])));
                $sheet->setCellValue(chr(ord($Scol) + 1) . $row, $evaluation['agent_name']);
                $sheet->setCellValue(chr(ord($Scol) + 2) . $row, $evaluation['activity_name']);
                $sheet->setCellValue(chr(ord($Scol) + 3) . $row, $evaluation['evaluator_name']);
                $sheet->setCellValue(chr(ord($Scol) + 4) . $row, $evaluation['score_total'] . '%');
                break;
            case 'agent':
                $sheet->setCellValue($Scol . $row, date('Y-m-d', strtotime($evaluation['evaluation_date'])));
                $sheet->setCellValue(chr(ord($Scol) + 1) . $row, $evaluation['activity_name']);
                $sheet->setCellValue(chr(ord($Scol) + 2) . $row, $evaluation['evaluator_name']);
                $sheet->setCellValue(chr(ord($Scol) + 3) . $row, $evaluation['score_total'] . '%');
                $sheet->setCellValue(chr(ord($Scol) + 4) . $row, $evaluation['comments']);
                break;
            case 'activity':
                $sheet->setCellValue($Scol . $row, date('Y-m-d', strtotime($evaluation['evaluation_date'])));
                $sheet->setCellValue(chr(ord($Scol) + 1) . $row, $evaluation['agent_name']);
                $sheet->setCellValue(chr(ord($Scol) + 2) . $row, $evaluation['evaluator_name']);
                $sheet->setCellValue(chr(ord($Scol) + 3) . $row, $evaluation['score_total'] . '%');
                $sheet->setCellValue(chr(ord($Scol) + 4) . $row, $evaluation['comments']);
                break;
        }
        $row++;
    }

    // Apply data style
    if ($row > $startRow) {
        $sheet->getStyle($Scol . $startRow . ':' . chr(ord($Scol) + 4) . ($row - 1))->applyFromArray($dataStyle);
    }

    // Auto-size columns
    foreach (range($Scol, chr(ord($Scol) + 4)) as $col) {
        $sheet->getColumnDimension($col)->setAutoSize(true);
    }

    // Add charts based on report type
    $chartRow = $row + 2;
    $sheet->setCellValue($Scol . $chartRow, 'Charts');
    $sheet->getStyle($Scol . $chartRow)->getFont()->setBold(true)->setSize(14);
    $chartRow += 2;

    // Create a new worksheet for charts
    $chartSheet = $spreadsheet->createSheet();
    $chartSheet->setTitle('Charts');

    // Define starting position for chart sheet
    $chartScol = 'C';
    $chartSrow = 5;

    switch ($type) {
        case 'business_unit':
            // Add chart headers for business unit report
            $chartSheet->setCellValue($chartScol . $chartSrow, 'Activity');
            $chartSheet->setCellValue(chr(ord($chartScol) + 1) . $chartSrow, 'Score');
            $chartSheet->setCellValue(chr(ord($chartScol) + 3) . $chartSrow, 'Agent');
            $chartSheet->setCellValue(chr(ord($chartScol) + 4) . $chartSrow, 'Score');

            // Apply header style to chart headers
            $chartSheet->getStyle($chartScol . $chartSrow . ':' . chr(ord($chartScol) + 1) . $chartSrow)->applyFromArray($headerStyle);
            $chartSheet->getStyle(chr(ord($chartScol) + 3) . $chartSrow . ':' . chr(ord($chartScol) + 4) . $chartSrow)->applyFromArray($headerStyle);

            // Add activity scores data for chart
            $chartCol = chr(ord($Scol) + 4); // Start from column G (assuming $Scol is 'C')
            $sheet->setCellValue($chartCol . ($chartRow - 1), 'Activity Scores');
            $sheet->getStyle($chartCol . ($chartRow - 1))->getFont()->setBold(true);

            $sheet->setCellValue($chartCol . $chartRow, 'Activity');
            $sheet->setCellValue(chr(ord($chartCol) + 1) . $chartRow, 'Average Score');
            $sheet->getStyle($chartCol . $chartRow . ':' . chr(ord($chartCol) + 1) . $chartRow)->applyFromArray($headerStyle);

            $activityRow = $chartRow + 1;
            $activityStartRow = $activityRow;
            foreach ($data['activityScores'] as $activityScore) {
                $sheet->setCellValue($chartCol . $activityRow, $activityScore['activity_name']);
                $sheet->setCellValue(chr(ord($chartCol) + 1) . $activityRow, $activityScore['average_score']);
                $activityRow++;
            }
            $activityEndRow = $activityRow - 1;

            // Create activity chart
            $chartSheet->setCellValue($chartScol . ($chartSrow - 2), 'Activity Scores Chart');
            $chartSheet->getStyle($chartScol . ($chartSrow - 2))->getFont()->setBold(true)->setSize(14);

            // Copy activity data to chart sheet
            for ($i = $activityStartRow; $i <= $activityEndRow; $i++) {
                $activityName = $sheet->getCell($chartCol . $i)->getValue();
                $activityScore = $sheet->getCell(chr(ord($chartCol) + 1) . $i)->getValue();
                $chartSheet->setCellValue($chartScol . ($i - $activityStartRow + $chartSrow + 1), $activityName);
                $chartSheet->setCellValue(chr(ord($chartScol) + 1) . ($i - $activityStartRow + $chartSrow + 1), $activityScore);
            }

            // Create activity chart
            $dataSeriesLabels = [
                new DataSeriesValues(DataSeriesValues::DATASERIES_TYPE_STRING, 'Charts!$' . chr(ord($chartScol) + 1) . '$' . $chartSrow, null, 1),
            ];

            $xAxisTickValues = [
                new DataSeriesValues(DataSeriesValues::DATASERIES_TYPE_STRING, 'Charts!$' . $chartScol . '$' . ($chartSrow + 1) . ':$' . $chartScol . '$' . ($chartSrow + $activityEndRow - $activityStartRow + 1), null, $activityEndRow - $activityStartRow + 1),
            ];

            $dataSeriesValues = [
                new DataSeriesValues(DataSeriesValues::DATASERIES_TYPE_NUMBER, 'Charts!$' . chr(ord($chartScol) + 1) . '$' . ($chartSrow + 1) . ':$' . chr(ord($chartScol) + 1) . '$' . ($chartSrow + $activityEndRow - $activityStartRow + 1), null, $activityEndRow - $activityStartRow + 1),
            ];

            $series = new DataSeries(
                DataSeries::TYPE_BARCHART,
                DataSeries::GROUPING_STANDARD,
                range(0, count($dataSeriesValues) - 1),
                $dataSeriesLabels,
                $xAxisTickValues,
                $dataSeriesValues
            );
            $series->setPlotDirection(DataSeries::DIRECTION_VERTICAL);

            $plotArea = new PlotArea(null, [$series]);
            $legend = new Legend(Legend::POSITION_RIGHT, null, false);

            $title = new Title('Activity Scores');
            $xAxisLabel = new Title('Activities');
            $yAxisLabel = new Title('Score (%)');

            $chart = new Chart(
                'activity_chart',
                $title,
                $legend,
                $plotArea,
                true,
                '0',
                $xAxisLabel,
                $yAxisLabel
            );

            // Position the chart to the left of the data tables
            $chart->setTopLeftPosition('L5');
            $chart->setBottomRightPosition('S20');

            $chartSheet->addChart($chart);

            // Add agent scores data for chart
            $agentCol = chr(ord($chartCol) + 3); // Start from column J (assuming $chartCol is 'G')
            $sheet->setCellValue($agentCol . ($chartRow - 1), 'Agent Scores');
            $sheet->getStyle($agentCol . ($chartRow - 1))->getFont()->setBold(true);

            $sheet->setCellValue($agentCol . $chartRow, 'Agent');
            $sheet->setCellValue(chr(ord($agentCol) + 1) . $chartRow, 'Average Score');
            $sheet->getStyle($agentCol . $chartRow . ':' . chr(ord($agentCol) + 1) . $chartRow)->applyFromArray($headerStyle);

            $agentRow = $chartRow + 1;
            $agentStartRow = $agentRow;
            foreach ($data['agentScores'] as $agentScore) {
                $sheet->setCellValue($agentCol . $agentRow, $agentScore['agent_name']);
                $sheet->setCellValue(chr(ord($agentCol) + 1) . $agentRow, $agentScore['average_score']);
                $agentRow++;
            }
            $agentEndRow = $agentRow - 1;

            // Copy agent data to chart sheet
            for ($i = $agentStartRow; $i <= $agentEndRow; $i++) {
                $agentName = $sheet->getCell($agentCol . $i)->getValue();
                $agentScore = $sheet->getCell(chr(ord($agentCol) + 1) . $i)->getValue();
                $chartSheet->setCellValue(chr(ord($chartScol) + 3) . ($i - $agentStartRow + $chartSrow + 1), $agentName);
                $chartSheet->setCellValue(chr(ord($chartScol) + 4) . ($i - $agentStartRow + $chartSrow + 1), $agentScore);
            }

            // Create agent chart
            $dataSeriesLabels2 = [
                new DataSeriesValues(DataSeriesValues::DATASERIES_TYPE_STRING, 'Charts!$' . chr(ord($chartScol) + 4) . '$' . $chartSrow, null, 1),
            ];

            $xAxisTickValues2 = [
                new DataSeriesValues(DataSeriesValues::DATASERIES_TYPE_STRING, 'Charts!$' . chr(ord($chartScol) + 3) . '$' . ($chartSrow + 1) . ':$' . chr(ord($chartScol) + 3) . '$' . ($chartSrow + $agentEndRow - $agentStartRow + 1), null, $agentEndRow - $agentStartRow + 1),
            ];

            $dataSeriesValues2 = [
                new DataSeriesValues(DataSeriesValues::DATASERIES_TYPE_NUMBER, 'Charts!$' . chr(ord($chartScol) + 4) . '$' . ($chartSrow + 1) . ':$' . chr(ord($chartScol) + 4) . '$' . ($chartSrow + $agentEndRow - $agentStartRow + 1), null, $agentEndRow - $agentStartRow + 1),
            ];

            $series2 = new DataSeries(
                DataSeries::TYPE_BARCHART,
                DataSeries::GROUPING_STANDARD,
                range(0, count($dataSeriesValues2) - 1),
                $dataSeriesLabels2,
                $xAxisTickValues2,
                $dataSeriesValues2
            );
            $series2->setPlotDirection(DataSeries::DIRECTION_VERTICAL);

            $plotArea2 = new PlotArea(null, [$series2]);
            $legend2 = new Legend(Legend::POSITION_RIGHT, null, false);

            $title2 = new Title('Agent Scores');
            $xAxisLabel2 = new Title('Agents');
            $yAxisLabel2 = new Title('Score (%)');

            $chart2 = new Chart(
                'agent_chart',
                $title2,
                $legend2,
                $plotArea2,
                true,
                '0',
                $xAxisLabel2,
                $yAxisLabel2
            );

            // Position the chart to the left of the data tables
            $chart2->setTopLeftPosition('L25');
            $chart2->setBottomRightPosition('S40');

            $chartSheet->addChart($chart2);
            break;

        case 'agent':
            // Add chart headers for agent report
            $chartSheet->setCellValue($chartScol . $chartSrow, 'Date');
            $chartSheet->setCellValue(chr(ord($chartScol) + 1) . $chartSrow, 'Score');

            // Add daily averages data for chart
            $chartCol = chr(ord($Scol) + 4); // Start from column G (assuming $Scol is 'C')
            $sheet->setCellValue($chartCol . ($chartRow - 1), 'Score Trend');
            $sheet->getStyle($chartCol . ($chartRow - 1))->getFont()->setBold(true);

            $sheet->setCellValue($chartCol . $chartRow, 'Date');
            $sheet->setCellValue(chr(ord($chartCol) + 1) . $chartRow, 'Average Score');
            $sheet->getStyle($chartCol . $chartRow . ':' . chr(ord($chartCol) + 1) . $chartRow)->applyFromArray($headerStyle);

            $dateRow = $chartRow + 1;
            $dateStartRow = $dateRow;
            foreach ($data['dailyAverages'] as $date => $score) {
                $sheet->setCellValue($chartCol . $dateRow, $date);
                $sheet->setCellValue(chr(ord($chartCol) + 1) . $dateRow, $score);
                $dateRow++;
            }
            $dateEndRow = $dateRow - 1;

            // Create score trend chart
            $chartSheet->setCellValue($chartScol . ($chartSrow - 2), 'Score Trend Chart');
            $chartSheet->getStyle($chartScol . ($chartSrow - 2))->getFont()->setBold(true)->setSize(14);

            // Copy date data to chart sheet
            for ($i = $dateStartRow; $i <= $dateEndRow; $i++) {
                $date = $sheet->getCell($chartCol . $i)->getValue();
                $score = $sheet->getCell(chr(ord($chartCol) + 1) . $i)->getValue();
                $chartSheet->setCellValue($chartScol . ($i - $dateStartRow + $chartSrow + 1), $date);
                $chartSheet->setCellValue(chr(ord($chartScol) + 1) . ($i - $dateStartRow + $chartSrow + 1), $score);
            }

            // Create line chart
            $dataSeriesLabels = [
                new DataSeriesValues(DataSeriesValues::DATASERIES_TYPE_STRING, 'Charts!$' . chr(ord($chartScol) + 1) . '$' . $chartSrow, null, 1),
            ];

            $xAxisTickValues = [
                new DataSeriesValues(DataSeriesValues::DATASERIES_TYPE_STRING, 'Charts!$' . $chartScol . '$' . ($chartSrow + 1) . ':$' . $chartScol . '$' . ($chartSrow + $dateEndRow - $dateStartRow + 1), null, $dateEndRow - $dateStartRow + 1),
            ];

            $dataSeriesValues = [
                new DataSeriesValues(DataSeriesValues::DATASERIES_TYPE_NUMBER, 'Charts!$' . chr(ord($chartScol) + 1) . '$' . ($chartSrow + 1) . ':$' . chr(ord($chartScol) + 1) . '$' . ($chartSrow + $dateEndRow - $dateStartRow + 1), null, $dateEndRow - $dateStartRow + 1),
            ];

            $series = new DataSeries(
                DataSeries::TYPE_LINECHART,
                DataSeries::GROUPING_STANDARD,
                range(0, count($dataSeriesValues) - 1),
                $dataSeriesLabels,
                $xAxisTickValues,
                $dataSeriesValues
            );

            $plotArea = new PlotArea(null, [$series]);
            $legend = new Legend(Legend::POSITION_RIGHT, null, false);

            $title = new Title('Score Trend Over Time');
            $xAxisLabel = new Title('Date');
            $yAxisLabel = new Title('Score (%)');

            $chart = new Chart(
                'score_trend_chart',
                $title,
                $legend,
                $plotArea,
                true,
                '0',
                $xAxisLabel,
                $yAxisLabel
            );

            // Position the chart to the left of the data tables
            $chart->setTopLeftPosition('L5');
            $chart->setBottomRightPosition('S20');

            $chartSheet->addChart($chart);
            break;

        case 'activity':
            // Add chart headers for activity report
            $chartSheet->setCellValue($chartScol . $chartSrow, 'Agent');
            $chartSheet->setCellValue(chr(ord($chartScol) + 1) . $chartSrow, 'Score');
            $chartSheet->setCellValue(chr(ord($chartScol) + 2) . $chartSrow, 'Count');

            // Add agent scores data for chart
            $chartCol = chr(ord($Scol) + 4); // Start from column G (assuming $Scol is 'C')
            $sheet->setCellValue($chartCol . ($chartRow - 1), 'Agent Scores');
            $sheet->getStyle($chartCol . ($chartRow - 1))->getFont()->setBold(true);

            $sheet->setCellValue($chartCol . $chartRow, 'Agent');
            $sheet->setCellValue(chr(ord($chartCol) + 1) . $chartRow, 'Average Score');
            $sheet->setCellValue(chr(ord($chartCol) + 2) . $chartRow, 'Evaluation Count');
            $sheet->getStyle($chartCol . $chartRow . ':' . chr(ord($chartCol) + 2) . $chartRow)->applyFromArray($headerStyle);

            $agentRow = $chartRow + 1;
            $agentStartRow = $agentRow;
            foreach ($data['agentScores'] as $agentScore) {
                $sheet->setCellValue($chartCol . $agentRow, $agentScore['agent_name']);
                $sheet->setCellValue(chr(ord($chartCol) + 1) . $agentRow, $agentScore['average_score']);
                $sheet->setCellValue(chr(ord($chartCol) + 2) . $agentRow, $agentScore['evaluation_count']);
                $agentRow++;
            }
            $agentEndRow = $agentRow - 1;

            // Create agent performance chart
            $chartSheet->setCellValue($chartScol . ($chartSrow - 2), 'Agent Performance Chart');
            $chartSheet->getStyle($chartScol . ($chartSrow - 2))->getFont()->setBold(true)->setSize(14);

            // Copy agent data to chart sheet
            for ($i = $agentStartRow; $i <= $agentEndRow; $i++) {
                $agentName = $sheet->getCell($chartCol . $i)->getValue();
                $agentScore = $sheet->getCell(chr(ord($chartCol) + 1) . $i)->getValue();
                $evaluationCount = $sheet->getCell(chr(ord($chartCol) + 2) . $i)->getValue();
                $chartSheet->setCellValue($chartScol . ($i - $agentStartRow + $chartSrow + 1), $agentName);
                $chartSheet->setCellValue(chr(ord($chartScol) + 1) . ($i - $agentStartRow + $chartSrow + 1), $agentScore);
                $chartSheet->setCellValue(chr(ord($chartScol) + 2) . ($i - $agentStartRow + $chartSrow + 1), $evaluationCount);
            }

            // Create combined chart (bar + line)
            $dataSeriesLabels1 = [
                new DataSeriesValues(DataSeriesValues::DATASERIES_TYPE_STRING, 'Charts!$' . chr(ord($chartScol) + 1) . '$' . $chartSrow, null, 1),
            ];

            $dataSeriesLabels2 = [
                new DataSeriesValues(DataSeriesValues::DATASERIES_TYPE_STRING, 'Charts!$' . chr(ord($chartScol) + 2) . '$' . $chartSrow, null, 1),
            ];

            $xAxisTickValues = [
                new DataSeriesValues(DataSeriesValues::DATASERIES_TYPE_STRING, 'Charts!$' . $chartScol . '$' . ($chartSrow + 1) . ':$' . $chartScol . '$' . ($chartSrow + $agentEndRow - $agentStartRow + 1), null, $agentEndRow - $agentStartRow + 1),
            ];

            $dataSeriesValues1 = [
                new DataSeriesValues(DataSeriesValues::DATASERIES_TYPE_NUMBER, 'Charts!$' . chr(ord($chartScol) + 1) . '$' . ($chartSrow + 1) . ':$' . chr(ord($chartScol) + 1) . '$' . ($chartSrow + $agentEndRow - $agentStartRow + 1), null, $agentEndRow - $agentStartRow + 1),
            ];

            $dataSeriesValues2 = [
                new DataSeriesValues(DataSeriesValues::DATASERIES_TYPE_NUMBER, 'Charts!$' . chr(ord($chartScol) + 2) . '$' . ($chartSrow + 1) . ':$' . chr(ord($chartScol) + 2) . '$' . ($chartSrow + $agentEndRow - $agentStartRow + 1), null, $agentEndRow - $agentStartRow + 1),
            ];

            $series1 = new DataSeries(
                DataSeries::TYPE_BARCHART,
                DataSeries::GROUPING_STANDARD,
                range(0, count($dataSeriesValues1) - 1),
                $dataSeriesLabels1,
                $xAxisTickValues,
                $dataSeriesValues1
            );
            $series1->setPlotDirection(DataSeries::DIRECTION_VERTICAL);

            $series2 = new DataSeries(
                DataSeries::TYPE_LINECHART,
                DataSeries::GROUPING_STANDARD,
                range(0, count($dataSeriesValues2) - 1),
                $dataSeriesLabels2,
                $xAxisTickValues,
                $dataSeriesValues2
            );

            $plotArea = new PlotArea(null, [$series1, $series2]);
            $legend = new Legend(Legend::POSITION_RIGHT, null, false);

            $title = new Title('Agent Performance');
            $xAxisLabel = new Title('Agents');
            $yAxisLabel = new Title('Score (%)');

            $chart = new Chart(
                'agent_performance_chart',
                $title,
                $legend,
                $plotArea,
                true,
                '0',
                $xAxisLabel,
                $yAxisLabel
            );

            // Position the chart to the left of the data tables
            $chart->setTopLeftPosition('L5');
            $chart->setBottomRightPosition('S20');

            $chartSheet->addChart($chart);
            break;
    }

    // Set active sheet to the first sheet before creating the writer
    $spreadsheet->setActiveSheetIndex(0);

    // Create writer with specific settings for charts
    $writer = new Xlsx($spreadsheet);

    // Set writer options to include charts
    $writer->setIncludeCharts(true);

    // Set headers for download
    header('Content-Type: application/vnd.openxmlformats-officedocument.spreadsheetml.sheet');
    header('Content-Disposition: attachment;filename="' . $filename . '"');
    header('Cache-Control: max-age=0');

    // Save file to PHP output
    $writer->save('php://output');
    exit;
}

